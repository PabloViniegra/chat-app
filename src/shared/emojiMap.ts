/**
 * Type representing all valid emoji shortcodes
 */
export type EmojiShortcode = keyof typeof EMOJI_MAP;

/**
 * Type representing all emoji Unicode characters
 */
export type EmojiUnicode = (typeof EMOJI_MAP)[EmojiShortcode];

/**
 * Get emoji Unicode from shortcode with type-safe overload for known shortcodes
 * @param shortcode - Emoji shortcode (e.g., "smile")
 * @returns Emoji Unicode character or undefined if not found
 */
export function getEmoji<T extends EmojiShortcode>(
	shortcode: T,
): (typeof EMOJI_MAP)[T];
export function getEmoji(shortcode: string): string | undefined;
export function getEmoji(shortcode: string): string | undefined {
	return EMOJI_MAP[shortcode as EmojiShortcode];
}

/**
 * Get all available emoji shortcodes
 */
export function getAllShortcodes() {
	return Object.keys(EMOJI_MAP) as EmojiShortcode[];
}

/**
 * Check if a shortcode exists in the emoji map (type guard)
 * @param shortcode - String to check
 * @returns True if shortcode is valid, with type narrowing
 */
export function hasEmoji(shortcode: string): shortcode is EmojiShortcode {
	return shortcode in EMOJI_MAP;
}

